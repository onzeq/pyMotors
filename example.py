"""Simple test for using adafruit_motorkit with a DC motor"""
import time
import board
from adafruit_motorkit import MotorKit

kit = MotorKit(i2c=board.I2C())
m = kit.motor2
m.throttle = 1.0
time.sleep(0.5)
m.throttle = 0
